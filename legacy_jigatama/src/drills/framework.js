let recent = [];
function genFramework(level) {
  const band = level <= 4 ? [1, 2] : level <= 8 ? [1, 2, 3] : [2, 3];
  let pool = BANK.filter((q) => band.includes(q.tier) && !recent.includes(q));
  if (pool.length === 0) { recent = []; pool = BANK.filter((q) => band.includes(q.tier)); }
  const item = pick(pool);
  recent.push(item);
  if (recent.length > 8) recent.shift();

  // 選択肢の順番をシャッフルしても正解を追跡
  const correct = item.opts[item.ans];
  const choices = shuffle(item.opts);
  return {
    type: 'choice', pillar: P,
    prompt: `<span style="font-size:20px">${item.q}</span><small>［${item.tag}］</small>`,
    choices, answerIndex: choices.indexOf(correct),
    explanation: item.exp,
    expectedMs: 22000,
  };
}

export const frameworkDrills = [
  { id: 'framework.core', pillar: P, title: '思考の型', icon: '🧠', blurb: 'MECE・仮説・期待値など思考の道具', generate: genFramework },
];
